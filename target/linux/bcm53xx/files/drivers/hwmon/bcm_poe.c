#define POE_ATTR_RO(_name, _func) __ATTR(_name, 0444, _func, NULL)
#define POE_ATTR_WO(_name, _func) __ATTR(_name, 0222, NULL,  _func)
#define POE_ATTR_RW(_name, _show, _store) __ATTR(_name, 0666, _show, _store)

#define POE_ATTR_RO_AUTO(_name) POE_ATTR_RO(_name, _name##_show)
#define POE_ATTR_WO_AUTO(_name) POE_ATTR_WO(_name, _name##_store)
#define POE_ATTR_RW_AUTO(_name) POE_ATTR_RW(_name, _name##_show,  _name##_store)

#define POE_ATTR_RO_FUNC(_name, _reg, _bits, _bitoff, _pstr, _type) \
static ssize_t _name##_show(struct _type *dev, struct _type##_attribute *attr, char *buf) { \
    s32 rd = (_reg >= 0 ? bcm_read(_reg) : -1); \
    int idx = ((rd >> _bitoff) & _bits); \
    return _pstr; \
}

#define _POE_PRINT(_str, _args) \
    scnprintf(buf, PAGE_SIZE, _str, _args)
#define POE_PRINT_STR(_strs) \
    _POE_PRINT("%s\n", (rd < 0 ? "ERR" : ((idx < ARRAY_SIZE(_strs)) ? _strs[idx] : "ERR")))
#define POE_PRINT_BITS \
    _POE_PRINT("%x\n", (rd < 0 ? -1 : idx))
#define POE_PRINT_MATCH(_mval) \
    _POE_PRINT("%d\n", ( rd < 0 ? -1 : (_mval == idx)));

#define DEV_RD(_name, _reg, _regoff, _bits, _bitoff) \
    POE_ATTR_RO_FUNC(_name, (_reg + (dev->devt * _regoff)), (_bits), (_bitoff), POE_PRINT_BITS, device)
#define DEV_SHARED_RD(_name, _reg, _regoff, _bits, _bitoff) \
    POE_ATTR_RO_FUNC(_name, (_reg + (dev->devt * _regoff)), (_bits), (_bitoff + (dev->devt)), POE_PRINT_BITS, device)
#define DEV_MATCH(_name, _reg, _regoff, _bits, _bitoff, _mval) \
    POE_ATTR_RO_FUNC(_name, (_reg + (dev->devt * _regoff)), (_bits), (_bitoff), POE_PRINT_MATCH(_mval), device)
#define DEV_SHARED_MATCH(_name, _reg, _regoff, _bits, _bitoff, _mval) \
    POE_ATTR_RO_FUNC(_name, (_reg + (dev->devt * _regoff)), (_bits), (_bitoff + (dev->devt)), POE_PRINT_MATCH(_mval), device)
#define DEV_STR_RD(_name, _reg, _regoff, _bits, _bitoff) \
    POE_ATTR_RO_FUNC(_name, (_reg + (dev->devt * _regoff)), (_bits), (_bitoff), POE_PRINT_STR(_name), device)

